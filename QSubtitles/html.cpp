    char16_t Special1252[] =
    {
        0x20AC, 0     , 0x201A, 0x0192,
        0x201E, 0x2026, 0x2020, 0x2021,
        0x02C6, 0x2030, 0x0160, 0x2039,
        0x0152, 0     , 0x017D, 0,
        0     , 0x2018, 0x2019, 0x201C,
        0x201D, 0x2022, 0x2013, 0x2014,
        0x02DC, 0x2122, 0x0161, 0x203A,
        0x0153, 0     , 0x017E, 0x0178,
    };

    struct RefComp
    {
        RefComp(int ci) : m_ci(ci) {};
        bool operator() (const HTMLRef& ref, QChar c) const {
            return ref.name[m_ci] < c;
        }
        bool operator() (QChar c, const HTMLRef& ref) const {
            return c < ref.name[m_ci];
        }
    private:
        int m_ci;
    };

    bool is_digit(QChar c, bool is_hex)
    {
        return c >= '0' && c <= '9' || is_hex && (c >= 'A' && c <= 'F' || c >= 'a' && c <= 'f');
    }

    QStringView consume_digits(QStringView str, qsizetype& pos, bool is_hex)
    {
        qsizetype start_pos = pos;
        while (pos < str.size() && is_digit(str[pos], is_hex))
        {
            ++pos;
        }
        return str.mid(start_pos, pos - start_pos);
    }

    QString consume_number_char_ref(QStringView str, qsizetype& pos)
    {
        QString result;
        qsizetype start_pos = pos;
        ++pos; // consume the '#'
        if (pos < str.size())
        {
            QChar c = str[pos];
            bool is_hex = false;
            if (c == 'X' || c == 'x')
            {
                is_hex = true;
                // consume the 'X'
                ++pos;
            }
            QStringView digits = consume_digits(str, pos, is_hex);
            if (!digits.isEmpty())
            {
                if (pos < str.size() && str[pos] == ';')
                    ++pos;

                int number = digits.toInt(nullptr, is_hex ? 16 : 10);
                if (number == 0)
                {
                    number = 0xFFFD;
                }
                else if (number >= 0x80 && number <= 0x9F)
                {
                    char16_t spec = Special1252[number - 0x80];
                    if (spec != 0)
                        result = spec;
                }
                result.append(QChar::fromUcs4(number));
            }
            else
            {
                // unconsume the '#' and the 'X'
                pos = start_pos;
            }
        }
        else
        {
            // unconsume the '#'
            pos = start_pos;
        }
        return result;
    }

    QString consume_char_ref(QStringView str, qsizetype& pos)
    {
        QString result;

        if (pos >= str.size())
            return result;

        QChar c = str[pos];

        if (c == QChar::Tabulation ||
            c == QChar::LineFeed ||
            c == QChar::FormFeed ||
            c == QChar::Space ||
            c == '<' || c == '&')
            return result;

        if (c == '#')
        {
            return consume_number_char_ref(str, pos);
        }
        else
        {
            int ci = 0;

            const HTMLRef* a = std::begin(HTML_REFS);
            const HTMLRef* b = std::end(HTML_REFS);

            const char16_t* ref_chars = nullptr;

            while (str[pos] != '\0')
            {
                auto range = std::equal_range(a, b, str[pos], RefComp(ci));
                a = range.first;
                b = range.second;
                if (a == b)
                    break;

                ++pos;
                ++ci;

                if (a->name[ci] == '\0')
                    // Exact match. We need to look further if there longer reference
                    // e.g. "not" and "notin"
                    ref_chars = a->chars;
            }
            result = QString::fromUtf16(ref_chars);
        }
        return result;
    }

    QString consume_char_ref(QStringView str, qsizetype& pos, const std::set<QChar>& additional_allowed)
    {
        QString result;
        if (pos >= str.size())
            return result;

        QChar c = str[pos];

        if (additional_allowed.find(c) != additional_allowed.end())
            return result;
        else
            return consume_char_ref(str, pos);
    }

